    <section class="mt-5">
      <h2 class="TituloServiciosE text-center">Problemas Psicológicos Frecuentes en Niños</h2>
      <ul class="ParrafoServiciosE">
        <li><strong>Acoso escolar o bullying:</strong> Es el abuso o maltrato, sin motivo evidente, que sufre un menor
          intencionada y repetidamente por parte de uno o varios compañeros del ámbito académico. Puede ser maltrato
          físico, psíquico, social o verbal.</li>
        <li><strong>Amigos imaginarios:</strong> A pesar de ser un fenómeno que enriquece la imaginación y la
          interacción social, es importante observar la evolución del problema. Los “amigos imaginarios” suelen ser
          casos idealizados (duendes, hadas y superhéroes) o amistades invisibles con las que compartir sus juegos, sin
          embargo, se recomienda mantener atención en su evolución.</li>
        <li><strong>Fobia escolar:</strong> Es la incapacidad total o parcial del niño de acudir al colegio a causa de
          un miedo irracional sobre algún aspecto de la situación escolar que le crea un nivel de ansiedad elevado.</li>
        <li><strong>Trastorno del Espectro Autista:</strong> Es un trastorno neuropsiquiátrico que se caracteriza por el
          aislamiento social, las dificultades en la comunicación y los patrones estereotipados de conducta.</li>
        <li><strong>Depresión infantil:</strong> Es un trastorno que se caracteriza por una alteración en el estado de
          ánimo en el menor que puede llegar a sumirse en un estado de tristeza profundo o padecer bruscos cambios de
          humor.</li>
        <li><strong>Divorcio de los progenitores:</strong> El divorcio de los padres provoca en el menor un impacto
          emocional, que puede provocar diferentes actitudes que afecten a distintos ámbitos de su alrededor:
          sociabilidad, autoestima, estudio, comportamiento, etc.</li>
        <li><strong>Tics nerviosos:</strong> Son movimientos involuntarios bruscos, cortos y repetitivos de cualquier
          grupo muscular. Las causas de este trastorno pueden ser físicas o psicológicas y, en consecuencia, pueden
          provocar problemas conductuales y sociales. Generalmente, este trastorno desaparece a los meses.</li>
        <li><strong>Trastorno por déficit de atención e hiperactividad infantil (TDAH):</strong> Es un trastorno del
          comportamiento que se caracteriza por distracción, períodos de atención transitoria, inquietud e inestabilidad
          emocional, hiperactividad y conductas impulsivas.</li>
        <li>Además, otros comunes en los niños son los vinculados al sueño, los terrores nocturnos, los miedos en
          general, la alimentación, control de esfínteres (enuresis y encopresis), celos, traumas, impulsividad, la
          actividad (hiperquinesia, tics) y el lenguaje (tartamudeo, afasia y otros).</li>
      </ul>
